#*   unsigned int filtrar ( unsigned char a, unsigned char b,
#*                          unsigned int n, unsigned char v[]) ;
#!   a0 - a; a1 - b;  a2 - n; a3 - v[ ];
#  se v[i] <= a  then v[i] = 0
#  se v[i] >= b  then v[i] = 255
#  se a < v[i] < b then v[i] = v[i]
#  se a !< b continue
.text
.global filtrar

filtrar : 
addi sp,sp,-16
sw   ra,12(sp)
addi t0, a2, 0          # load n into temp counter;
bgeu a0, a1, Lend   # se a !< b end it;
li   t6, 255

Lprep :
beqz a2, Lend       # if no one is left, skip to end;
lbu  t1,0(a3)        # load a number in;
addi a2,a2,-1       # deduct 1 to the number of elemments;

L1 :
bleu t1, a0, Lcon1   # se v[i] <= a  then v[i] = 0;
bgeu t1, a1, Lcon2   # se v[i] >= b  then v[i] = 255;
# se codigo chega a esta linha con3 é realizada
# se a < v[i] < b then v[i] = v[i]
addi a3,a3,1        # progress the adress;
j    Lprep           # so code rolls back :)

Lcon1 :
sb zero, 0(a3)         # v[i] = 0;   store 0 in place of old v[i]; 
addi t0,t0,-1         # progress counter;
addi a3,a3,1        # progress the adress;
j Lprep

Lcon2 :
sb t6, 0(a3)        # v[i] = 255;   store 255 in place of old v[i];
addi t0,t0,-1        # progress counter;
addi a3,a3,1        # progress the adress;
j Lprep

Lend :

mv a0, t0           #a0 = t0 
lw ra, 12(sp)
addi sp, sp, 16
ret

