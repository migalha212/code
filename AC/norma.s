#* float norma ( float v[] , unsigned int p) ;
#* a0 - adress to v[];   a1 - p;
#* lenght 3;
#! raizN, receives real number in fa0 and n in a0;

.text
.global norma
norma :
addi sp,sp,-16
sw   ra,12(sp)
beqz a1, Lmax   # if p == 0, the maximum is used instead;
li   t0, 3
li   t6, 1
L0 :
    beqz t0, Lnext    # counter for members
    flw fa1, 0(a0)    # load a number;
    addi a0, a0, 4    # progress in the adress;
    addi t0,t0,-1     # reduce the counter;
    beq a1, t6, L2     # skip ahead if p == 1;
    addi t1, a1, 0    # load a counter

L1 :
    beq  t1, t6, L2    # skip ahead when p == 1;
    fmul.s fa1, fa1, fa1 #  fa1 = fa1 * fa1; (squared)
    addi t1,t1,-1
    j L1           # loop back;

L2 :
# ! this might fail :)
    fsgnj.s ft1, ft1, ft6 # replace the sign of ft1 with that of ft6 and assign to ft1;
    fadd.s ft6,ft6,ft1    # add everything onto ft6
    j L0

Lnext :
    sw a0, 8(sp)
    mv a0, a1
    fmv.s fa0, ft6
    call raizN              # in fa0 is now
    j Lend

Lmax :


Lend :
    fmv.x.s a0,fa0        #! another one that might fail
    lw ra,12(sp)
    addi sp,sp,16
    ret

